-- 1. 테이블 생성
CREATE TABLE IF NOT EXISTS chaebols (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  name_en TEXT NOT NULL,
  rank INT NOT NULL,
  total_assets_trillion NUMERIC,
  holding_company TEXT,
  chairman TEXT,
  color TEXT NOT NULL DEFAULT '#6b7280',
  created_at TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS companies (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  chaebol_id UUID NOT NULL REFERENCES chaebols(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  ticker TEXT,
  stock_code TEXT,
  entity_type TEXT NOT NULL CHECK (entity_type IN ('holding', 'subsidiary', 'individual')),
  krx_sector TEXT,
  gics_sector TEXT,
  market_cap_billion NUMERIC,
  stock_price NUMERIC,
  price_change_percent NUMERIC,
  is_listed BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS ownership_links (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  chaebol_id UUID NOT NULL REFERENCES chaebols(id) ON DELETE CASCADE,
  source_company_id UUID NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  target_company_id UUID NOT NULL REFERENCES companies(id) ON DELETE CASCADE,
  ownership_percent NUMERIC NOT NULL CHECK (ownership_percent >= 0 AND ownership_percent <= 100),
  ownership_type TEXT NOT NULL CHECK (ownership_type IN ('direct', 'indirect')),
  created_at TIMESTAMPTZ DEFAULT now()
);

-- RLS 활성화 (읽기 전용 공개)
ALTER TABLE chaebols ENABLE ROW LEVEL SECURITY;
ALTER TABLE companies ENABLE ROW LEVEL SECURITY;
ALTER TABLE ownership_links ENABLE ROW LEVEL SECURITY;

CREATE POLICY "공개 읽기 - chaebols" ON chaebols FOR SELECT USING (true);
CREATE POLICY "공개 읽기 - companies" ON companies FOR SELECT USING (true);
CREATE POLICY "공개 읽기 - ownership_links" ON ownership_links FOR SELECT USING (true);

